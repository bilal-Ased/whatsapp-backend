file_path = "/Users/bilalmughal/Documents/Dev/outbound-backend/kati.json"
output_csv = "/Users/bilalmughal/Documents/Dev/outbound-backend/matches.csv"

def extract_last_6_digits(uid):
    digits = ''.join(c for c in str(uid) if c.isdigit())
    return digits[-6:] if len(digits) >= 6 else ""

# Read raw file content
with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
    content = f.read()

# Match all JSON-like objects
objects = re.findall(r'\{.*?\}', content, re.DOTALL)

print(f"🔍 Found {len(objects)} objects. Scanning for matches...")

# Build CSV data
rows = []

for i, obj_str in enumerate(objects):
    try:
        obj = json.loads(obj_str)
        uid = obj.get("UniqueID", "")
        last6 = extract_last_6_digits(uid)
        if last6 in number_list:
            ticket_number = obj.get("Ticket_number", "")
            subject = obj.get("email_subject", "").strip().replace('\n', ' ')
            rows.append((f"TN{last6}", ticket_number, subject))
            print(f"✅ MATCH: TN{last6} → {ticket_number}")
    except Exception:
        continue

# Write CSV
with open(output_csv, "w", newline="", encoding="utf-8") as csvfile:
    writer = csv.writer(csvfile)
    writer.writerow(["original_ticket", "Ticket_number", "subject"])
    writer.writerows(rows)

print(f"\n✅ DONE: {len(rows)} entries written to {output_csv}")
